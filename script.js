let idiomaActual = "es";
let proyectoActual = null;
let slideActual = 0;

const overlay = document.getElementById("modalOverlay");
const modalClose = document.getElementById("modalClose");
const modalCat = document.getElementById("modalCat");
const modalTitle = document.getElementById("modalTitle");
const modalSub = document.getElementById("modalSub");
const track = document.getElementById("carouselTrack");
const dotsWrapper = document.getElementById("carouselDots");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");


function armarProyecto(id, lang) {
  const base = mediasBase[id];
  const textos = traducciones[lang].proyectos[id];
  if (!textos) return null;

  return {
    _id: id,
    cat: textos.cat,
    title: textos.title,
    sub: textos.sub,
    slides: base.slides.map((s, i) => ({
      ...s,
      backTitle: textos.slides[i].backTitle,
      backText: textos.slides[i].backText
    }))
  };
}

function changeTexts(lang) {
  const t = traducciones[lang];
  document.getElementById("htmlRoot").lang = lang;

  document.querySelectorAll("[data-lang]").forEach(el => {
    const clave = el.dataset.lang;
    if (t[clave]) {
      if (clave === "intro") el.style.whiteSpace = "pre-line";
      el.textContent = t[clave];
    }
  });

  document.documentElement.style.setProperty("--flip-hint", `"${t.flipHint}"`);

  
  const next = lang === "es" ? "en" : "es";
  
  
  document.getElementById("langLabel").textContent = next.toUpperCase();
  document.getElementById("langFlag").textContent = next === "en" ? "Esp" : "Eng";

  if (overlay.classList.contains("is-open") && proyectoActual) {
    proyectoActual = armarProyecto(proyectoActual._id, lang);
    modalCat.textContent = proyectoActual.cat;
    modalTitle.textContent = proyectoActual.title;
    modalSub.textContent = proyectoActual.sub;
    construirSlides();
    actualizarCarrusel();
  }
}

function abrirModal(id) {
  proyectoActual = armarProyecto(id, idiomaActual);
  if (!proyectoActual) return;

  slideActual = 0;
  modalCat.textContent = proyectoActual.cat;
  modalTitle.textContent = proyectoActual.title;
  modalSub.textContent = proyectoActual.sub;

  construirSlides();
  construirDots();
  actualizarCarrusel();

  overlay.classList.add("is-open");
  document.body.style.overflow = "hidden";
}

function construirSlides() {
  track.innerHTML = "";
  proyectoActual.slides.forEach((slide, i) => {
    const flipCard = document.createElement("div");
    flipCard.className = "flip-card";
    
    const inner = document.createElement("div");
    inner.className = "flip-card__inner";

    const front = document.createElement("div");
    front.className = "flip-card__front";

    if (slide.type === "video") {
      const video = document.createElement("video");
      video.src = slide.src;
      video.autoplay = video.muted = video.loop = video.playsInline = true;
      video.style.cssText = "width:100%;height:100%;object-fit:cover;";
      front.appendChild(video);
    } else {
      const img = document.createElement("img");
      img.src = slide.src;
      img.style.cssText = "width:100%;height:100%;object-fit:cover;";
      front.appendChild(img);
    }

    const back = document.createElement("div");
    back.className = "flip-card__back";
    back.innerHTML = `
      <span class="flip-back__tag">${proyectoActual.cat}</span>
      <h4 class="flip-back__title">${slide.backTitle}</h4>
      <p class="flip-back__text">${slide.backText}</p>
    `;

    inner.appendChild(front);
    inner.appendChild(back);
    flipCard.appendChild(inner);
    track.appendChild(flipCard);

    flipCard.addEventListener("mouseenter", () => inner.classList.add("is-flipped"));
    flipCard.addEventListener("mouseleave", () => inner.classList.remove("is-flipped"));
  });
}

function construirDots() {
  dotsWrapper.innerHTML = "";
  proyectoActual.slides.forEach((_, i) => {
    const dot = document.createElement("button");
    dot.className = "dot";
    dot.addEventListener("click", () => { slideActual = i; actualizarCarrusel(); });
    dotsWrapper.appendChild(dot);
  });
}

function actualizarCarrusel() {
  track.style.transform = `translateX(-${slideActual * 100}%)`;
  document.querySelectorAll(".dot").forEach((dot, i) => {
    dot.classList.toggle("dot--active", i === slideActual);
  });
  prevBtn.disabled = slideActual === 0;
  nextBtn.disabled = slideActual === proyectoActual.slides.length - 1;
}

function cerrarModal() {
  overlay.classList.remove("is-open");
  document.body.style.overflow = "";
  track.querySelectorAll("video").forEach(v => v.pause());
}

document.getElementById("langToggle").addEventListener("click", () => {
  idiomaActual = (idiomaActual === "es") ? "en" : "es";
  changeTexts(idiomaActual);
});

document.querySelectorAll(".project-card").forEach(card => {
  card.addEventListener("click", () => abrirModal(card.dataset.project));
});

modalClose.addEventListener("click", cerrarModal);
overlay.addEventListener("click", (e) => { if (e.target === overlay) cerrarModal(); });
prevBtn.addEventListener("click", () => { if (slideActual > 0) { slideActual--; actualizarCarrusel(); } });
nextBtn.addEventListener("click", () => { if (slideActual < proyectoActual.slides.length - 1) { slideActual++; actualizarCarrusel(); } });

document.addEventListener("keydown", (e) => {
  if (e.key === "Escape") cerrarModal();
  if (e.key === "ArrowLeft" && slideActual > 0) { slideActual--; actualizarCarrusel(); }
  if (e.key === "ArrowRight" && proyectoActual && slideActual < proyectoActual.slides.length - 1) { slideActual++; actualizarCarrusel(); }
});

let darkMode = false;

const lightTheme = {
  '--bg': '#ffffff',
  '--bgheader': '#000000',
  '--bgoscuro': '#0F1115',
  '--text': '#050505',
  '--accent': '#10e3e3',
  '--border': '#222',
  '--modal-bg': '#0a0a0a'
};

const darkTheme = {
  '--bg': '#0d1b2a',       
  '--bgheader': '#000814', 
  '--bgoscuro': '#1b263b',  
  '--text': '#e0e1dd',      
  '--accent': '#48cae4',    
  '--border': '#415a77',  
  '--modal-bg': '#1b263b'   
};


const themeBtn = document.getElementById('themeToggle');

if (themeBtn) {
  themeBtn.addEventListener("click", () => {
    darkMode = !darkMode;   
    
    const theme = darkMode ? darkTheme : lightTheme;
       
    Object.keys(theme).forEach(varName => {
      document.documentElement.style.setProperty(varName, theme[varName]);
    });

    themeBtn.textContent = darkMode ? '☀️' : '🌙';
  });
}

